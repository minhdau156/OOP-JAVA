package Lesson_4;

public class Pratice {
    public long plus(int num1, long num2) {
        long result = num1 + num2;
        return result;
    }
    public long plus(int num1, long num2,long num3) {
        long result = num1 + num2 + num3;
        return result;
    }
    public long plus(int num1, long num2, long num3, int num4) {
        long result = num1 + num2 + num3 + num4;
        return result;
    }
}
